        {
            R.id.CasesAcrossIndia->
            {
                val intent=Intent(this@StateWise,MainActivity::class.java)
                startActivity(intent)
                finish()
                return true
            }
            R.id.VaccineCertificate->
            {
                val intent=Intent(this@StateWise,DownloadCertificate::class.java)
                startActivity(intent)
                finish()
                return true
            }
            else->super.onOptionsItemSelected(item)
        }
    }

